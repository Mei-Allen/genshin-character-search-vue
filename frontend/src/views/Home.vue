<script setup>
import { ref, onMounted, watch } from 'vue'
import { useRouter } from 'vue-router'
import { apiUrl } from '../api'

const router = useRouter()
const searchQuery = ref('')
const suggestions = ref([])
const worldLayer = ref(null)
const epilogueRef = ref(null)
const searchRef = ref(null)

// 热门角色（与 v2 完全一致）
const popularCharacters = [
  { name: 'Venti', color: '#66cc99' },
  { name: 'Furina', color: '#4da6ff' },
  { name: 'Zhongli', color: '#c89b3c' },
  { name: 'Nahida', color: '#74b033' },
  { name: 'Hu Tao', color: '#ff6b6b' },
  { name: 'Kamisato Ayaka', color: '#8fe3ff' }
]

// 搜索防抖
let debounceTimer
watch(searchQuery, (val) => {
  clearTimeout(debounceTimer)
  if (val.length < 1) { suggestions.value = []; return }
  debounceTimer = setTimeout(async () => {
    try {
      const res = await fetch(apiUrl(`/characters?search=${encodeURIComponent(val)}&page_size=5`))
      const data = await res.json()
      suggestions.value = (data.data || []).map(d => d.character_name)
    } catch (e) { suggestions.value = [] }
  }, 200)
})

function doSearch() {
  if (searchQuery.value.trim()) {
    router.push(`/character/${encodeURIComponent(searchQuery.value.trim())}`)
  }
}

onMounted(() => {
  // IntersectionObserver：章节动画
  const chapterObserver = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
      if (entry.isIntersecting) entry.target.classList.add('show')
    })
  }, { threshold: 0.4 })

  document.querySelectorAll('.chapter').forEach(el => chapterObserver.observe(el))

  // 尾声 + 搜索区
  const epiObserver = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
      if (entry.isIntersecting) entry.target.classList.add('show')
    })
  }, { threshold: 0.4 })

  if (epilogueRef.value) epiObserver.observe(epilogueRef.value)
  if (searchRef.value) epiObserver.observe(searchRef.value)

  // 滚动时渐隐背景白色遮罩
  window.addEventListener('scroll', () => {
    if (!epilogueRef.value || !worldLayer.value) return
    const rect = epilogueRef.value.getBoundingClientRect()
    const start = window.innerHeight * 0.9
    const end = -window.innerHeight * 0.4
    let progress = (start - rect.top) / (start - end)
    progress = Math.max(0, Math.min(1, progress))
    const alpha = 0.8 * (1 - progress)
    worldLayer.value.style.background = `rgba(255,255,255,${alpha})`
  })
})
</script>

